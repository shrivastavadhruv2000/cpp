#include<iostream>
using namespace std;


int main()
{
	int a,b,c;
	cout<<"Enter the Value of a,b,c"<<endl;
	cin>>a>>b>>c;
	
	cout<<"Plus operator "<<endl;
	cout<<a+b+c<<endl;
	
	cout<<"Minus operator "<<endl;
	cout<<a-b-c<<endl;
	
	cout<<"Increment operator "<<endl;
	cout<<++a<<endl;
	
	cout<<"Decrement operator "<<endl;
	cout<<--b<<endl;
}

